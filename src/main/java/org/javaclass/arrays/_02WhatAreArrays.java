/*`
`                   An array is a contiguous block of memory storing a group of
`                   sequentially stored elements of the same type.
`                   Arrays in Java are of a fixed size and cannot be resized after declaration.
`*/

//-                         There are 3 ways to declare an array in Java

                    int[] intArray1 = {1, 2, 3, 4}; //explicit initialization
                    int[] intArray2 = new int[4];   //literal initialization
                    int intArray3[] = new int[5];   //not used

/*`
`                •	Each item of an array is called an element.
`                •	All elements of an array must be of the same type.
`                •	Items in an array are referenced via their index in square bracket notation,
`                   which begins with 0 for the first element.

 `*/
