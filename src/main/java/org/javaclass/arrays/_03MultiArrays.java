package org.javaclass.arrays;

public class _03MultiArrays {
//-                                        Multi Dimensional Arrays

/*`
`                   Multi Dimensional arrays are arrays that store other arrays.

`                   The most common form is the 2D array, which you can think of
`                   as a matrix or a table with rows and columns.
`
`                   There is no limit to how many dimensions an multi array can have.
`                   2D, 3D, 4D.....
`*/

//-                         There are 3 ways to declare a 2D array in Java

    int[][] intArray1 ={ {1, 2, 3, 4}, {5, 6, 7, 8}}; //explicit initialization
    int[][] intArray2 = new int[4][4];   //literal initialization
    int intArray3[][] = new int[5][4];   //not used

/*`
`                •	Each item of a multi array is called an element.
`                •	All elements of an array must be of the same type.
`                •	Items in a multi array are referenced via their index in square bracket notation,
`                   which begins with 0 for the first element.
 `*/
}



